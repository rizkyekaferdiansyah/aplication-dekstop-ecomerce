    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
if (tabel.getRowCount()== 0){
    JOptionPane.showMessageDialog(rootPane, "Isi data terlebih dahulu");
    }else if (tabel.getSelectedRow()== -1){
        JOptionPane.showMessageDialog(rootPane, "Pilih Data !!!");
    }else{    
String klik = tabel.getModel().getValueAt(tabel.getSelectedRow(), 0).toString();
String sql = "delete from tb_keranjang where id_transaksi=?";
int yakin = JOptionPane.showConfirmDialog(null,"Yakin Hapus Data","Konfirmasi",JOptionPane.YES_NO_OPTION);
if(yakin == JOptionPane.YES_NO_OPTION){
    try {
        ps = conn.prepareStatement(sql);
        ps.setString(1, klik);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(rootPane, "Selamat Data Berhasil Dihapus");
        sinkronTabel();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
        bayar.setText(null);
        uang.setText(null);
        kembalian.setText(null);
}}
    }//GEN-LAST:event_jButton6ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String total = bayar.getText();
        String uang1 = uang.getText();
        
        int total2 = Integer.parseInt(total);
        int uang2 = Integer.parseInt(uang1); 
        
        try{ 
            int kembali = (uang2 - total2);
            String kembali1 = Integer.toString(kembali);
            
            kembalian.setText(kembali1);
            JOptionPane.showMessageDialog(null, "Transaksi Berhasil!");
            
        }catch(NumberFormatException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Transaksi Tidak Valid");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


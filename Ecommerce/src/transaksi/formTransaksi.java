    private void jumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jumlahKeyReleased
        if(jumlah.getText().equals("")){
            totalharga.setText(null);
        }else{
        
        String harga1 = harga.getText();
        String jumlah1 = jumlah.getText();
    
        int harga2 = Integer.parseInt(harga1);
        int jumlah2 = Integer.parseInt(jumlah1);
        
        try{
        
        int total = harga2 * jumlah2;
        String total_harga = Integer.toString(total);
        
        totalharga.setText(total_harga);
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }   
        }
    }//GEN-LAST:event_jumlahKeyReleased

